// Função auxiliar para buscar competência
export const buscarCompetencia = (codigo) => {
  return bnccCompetencias[codigo] || "Competência não encontrada";
};

// Função para filtrar por ano
export const filtrarPorAno = (ano) => {
  const padrao = new RegExp(`^E[IF]${ano}`);
  return Object.entries(bnccCompetencias)
    .filter(([codigo]) => padrao.test(codigo))
    .reduce((acc, [codigo, descricao]) => {
      acc[codigo] = descricao;
      return acc;
    }, {});
};

// Função para filtrar por disciplina
export const filtrarPorDisciplina = (disciplina) => {
  const siglas = {
    'portugues': 'LP',
    'matematica': 'MA',
    'ciencias': 'CI',
    'geografia': 'GE',
    'historia': 'HI',
    'arte': 'AR',
    'educacao-fisica': 'EF',
    'ensino-religioso': 'ER'
  };
  
  const sigla = siglas[disciplina.toLowerCase().replace(/\s+/g, '-')];
  if (!sigla) return {};
  
  return Object.entries(bnccCompetencias)
    .filter(([codigo]) => codigo.includes(sigla))
    .reduce((acc, [codigo, descricao]) => {
      acc[codigo] = descricao;
      return acc;
    }, {});
};

export default bnccCompetencias;
